mood = input("How are you today? ")



if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Well... We all have days like that.")
elif mood == "excited":
    print("Wow! Wasn't expecting that!")
elif mood == "relaxed":
    print("Than keep enjoying!")
else:
    print("Cheer up, mate!")